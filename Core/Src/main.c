void init_task(void) {
    fsm_update(FSM_STATE_INIT);
}

void wait_req_task(void) {
    if (led_is_on()) {
        led_off();
    }

    serial_read();
    if (serial_buff_is_ready()) {
        serial_parse_buff();
    }

    if (is_button_pressed()) {
        fsm_update(FSM_EVENT_BTN_PRESSED);
    }
}

void listening_task(void) {
    static _Bool prev_high = TRUE;
    static uint32_t start_time;

    if (!led_is_on()) {
        led_on();
    }

    hall_read_data(&hadc1);
    if (hall_data_is_ready()) {
        uint16_t data = hall_get_data();
        // uint16_t data = (uint16_t)rand()%0xfff; // test purpose only.
        serial_send_data(data);

        if (0xfff == data) {
            if (!prev_high) {
                prev_high = TRUE;
                start_time = HAL_GetTick();
            }

            if ((start_time - HAL_GetTick()) >= WARNING_TIME_TOLLERANCE) {
                fsm_update(FSM_EVENT_WARNING);
            }
        } else {
            prev_high = FALSE;
        }
    }

    if (is_button_pressed()) {
        fsm_update(FSM_EVENT_BTN_PRESSED);
    }
}

void pause_task(void) {
    HAL_StatusTypeDef status = HAL_TIM_Base_Start_IT(&htim6);

    if (timer_flag) {
        timer_flag = FALSE;
        led_toggle();
    }

    serial_read();
    if (serial_buff_is_ready()) {
        serial_parse_buff();
    }

    if (is_button_pressed()) {
        timer_flag = FALSE;
        HAL_TIM_Base_Stop_IT(&htim6);
        fsm_update(FSM_EVENT_BTN_PRESSED);
    }
}

void warning_task(void) {
    if (led_is_on()) {
        led_off();
    }

    serial_send("WARNING");

    if (is_button_pressed()) {
        fsm_update(FSM_EVENT_BTN_PRESSED);
    }
}

void error_task(void) {
    HAL_TIM_Base_Start_IT(&htim7);

    if (timer_flag) {
        timer_flag = FALSE;
        led_toggle();
    }

    serial_send("ERROR");

    if (is_button_pressed()) {
        timer_flag = FALSE;
        HAL_TIM_Base_Stop_IT(&htim7);
        NVIC_SystemReset();
    }
}
